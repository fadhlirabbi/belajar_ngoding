#include <iostream>
#include <cstdlib>
#include <conio.h>

using namespace std;

main(){
    //contoh for loop
    for (int y = 0; y < 5; y++) {
        cout << y <<endl;
    }

    //contoh while loop
    int i = 0;
    while (i < 5) {
        cout << i <<endl;
        i++;
    }

    //contoh do while 
    int x = 0;
    do {
        cout << x << endl;
        x++;
    } while (x < 5);
}

